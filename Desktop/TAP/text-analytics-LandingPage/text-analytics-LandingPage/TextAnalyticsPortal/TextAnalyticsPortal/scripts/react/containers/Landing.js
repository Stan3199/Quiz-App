function getSteps() {
  return ['UPLOAD DATA', 'SELECT DATA', 'CUSTOMIZE DATA'];
}

function getStepContent(step) {
  switch (step) {
    case 0:
      return 'Select Sample Data or Upload a data';
    case 1:
      return 'What is an ad group anyways?';
    case 2:
      return 'This is the bit I really care about!';
    default:
      return 'Unknown step';
  }
}

export default function CustomizedSteppers(props) {
  const classes = useStyles();
  const [activeStep, setActiveStep] = React.useState(0);
  const [useSample,setUseSample]=React.useState(false);
  const steps = getSteps();

  const handleNext = () => {
    setActiveStep((prevActiveStep) => prevActiveStep + 1);
  };

  const handleBack = () => {
    setActiveStep((prevActiveStep) => prevActiveStep - 1);
  };

  const handleReset = () => {
    setActiveStep(0);
  };

  return (
    <div className={classes.root} >
        <div className='top-nav'>
<img src={Logo.MAQLogo}></img>
      <Stepper alternativeLabel activeStep={activeStep} connector={<ColorlibConnector />}>
        {steps.map((label) => (
          <Step key={label}>
            <StepLabel StepIconComponent={ColorlibStepIcon}>{label}</StepLabel>
          </Step>
        ))}
      </Stepper>
      </div>
      <div className='content-landing'>
          <div className='image-div'>
            <img src={config.Cir} width='100%' height='auto'></img>
          </div>
          <div className='info-div'>

        {activeStep === 0?<><h1 className='title-text'> Text Analytics Playground</h1>
         <h3 className='description-text'>Discover meaningful insights for text feedback generated from various customer channels like social media comments, surveys, or internal support systems.</h3>
         <button className='upload-btn'  onClick={handleNext}>Get Started</button></>:null}
         {activeStep === 1?<><h1 className='title-text'> Upload data</h1>
        
        <div className='data-upload'>
        <div className='data-middle'><img src={config.Uploadicon}/><span>Upload Dataset</span></div>
        <input type='file' />
         
        </div>
        <h1>OR</h1>
        <div className='data-enter'>
          <textarea  id='data-area' placeholder='Enter each comment in separate line' ></textarea>
        </div>
        <h3>Do not have Dataset ?<a href='#' style={{textDecoration:'none'}}>Use Sample Data</a></h3>
        <div><button className='nav-button' onClick={handleBack}>BACK</button><button className='nav-button' disabled={true} onClick={handleNext}>NEXT</button></div>
        </>:null}
          <div>
        {activeStep === steps.length ? (
          <div>
            <Typography className={classes.instructions}>
            Your data has been been successfully connected
            </Typography>
            <Button onClick={handleReset} className={classes.button}>
              Reset
            </Button>
            <Button
             variant="contained"
             color="primary"
             onClick={props.action} className={classes.button}>
              Generate Insights
            </Button>
          </div>
        ) : (
          <div>
            
          </div>
        )}
      </div>
          </div>
 
</div>
    </div>
  );
}
